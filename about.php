<p>
The company 'Oil Rush' just found oil. Are you part of it? You better be, pal!
<br><br>
'Geodynamics' looks vulnerable. What are your strategies with this share?<br>
A) Dump it.<br>
B) Wait for it to go south, and pick 'em up when its on the floor.<br>
